<template>
  <div class="signup-page">
    <div class="form-box">
      <h1>Upload your photos</h1>
      <p class="desc">Add at least one photo to complete your profile</p>

      <div class="photo-grid">
        <div
          v-for="(photo, index) in photos"
          :key="index"
          class="photo-slot"
          @click="uploadPhoto(index)"
        >
          <img v-if="photo" :src="photo" alt="Uploaded" />
          <span v-else>+</span>
        </div>
      </div>

      <button class="finish-btn" @click="$router.push('/profile')">
        Finish
      </button>

      <p class="back">
        <a @click.prevent="$router.push('/signup/step2')" href="#">← Back</a>
      </p>

      <!-- Ukryte pole pliku -->
      <input
        ref="fileInput"
        type="file"
        accept="image/*"
        @change="handleFileChange"
        style="display: none"
      />
    </div>
  </div>
</template>

<script>
export default {
  name: 'SignupStep3',
  data() {
    return {
      photos: [null, null, null] // 3 sloty na zdjęcia
    }
  },
  methods: {
    uploadPhoto(index) {
      this.activeIndex = index
      this.$refs.fileInput.click()
    },
    handleFileChange(event) {
      const file = event.target.files[0]
      if (file) {
        const reader = new FileReader()
        reader.onload = e => {
          this.$set(this.photos, this.activeIndex, e.target.result)
        }
        reader.readAsDataURL(file)
      }
    }
  }
}
</script>

